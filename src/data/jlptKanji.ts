// Create a reverse lookup for kanji to JLPT level
export const KANJI_TO_JLPT: Record<string, keyof typeof JLPT_KANJI_LISTS> = {};

Object.entries(JLPT_KANJI_LISTS).forEach(([level, kanjiList]) => {
  kanjiList.forEach(kanji => {
    KANJI_TO_JLPT[kanji] = level as keyof typeof JLPT_KANJI_LISTS;
  });
});

// JLPT level counts for validation
export const JLPT_TOTALS = {
  N5: 79,
  N4: 166,
  N3: 367,
  N2: 367,
  N1: 1232,
} as const;

/**
 * Get JLPT level for a kanji character
 * @param kanji The kanji character to look up
 * @returns JLPT level (N5, N4, N3, N2, N1) or null if not found
 */
export function getJLPTLevel(kanji: string): keyof typeof JLPT_KANJI_LISTS | null {
  return KANJI_TO_JLPT[kanji] || null;
}

/**
 * Check if a kanji is in JLPT
 * @param kanji The kanji character to check
 * @returns true if the kanji is in any JLPT level
 */
export function isJLPTKanji(kanji: string): boolean {
  return kanji in KANJI_TO_JLPT;
}

/**
 * Get all kanji for a specific JLPT level
 * @param level JLPT level (N5, N4, N3, N2, N1)
 * @returns Array of kanji for that level
 */
export function getKanjiForLevel(level: keyof typeof JLPT_KANJI_LISTS): readonly string[] {
  return JLPT_KANJI_LISTS[level];
}