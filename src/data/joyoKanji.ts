// Create a reverse lookup for kanji to Joyo grade
export const KANJI_TO_JOYO: Record<string, keyof typeof JOYO_KANJI_LISTS> = {};

Object.entries(JOYO_KANJI_LISTS).forEach(([grade, kanjiList]) => {
  kanjiList.forEach(kanji => {
    KANJI_TO_JOYO[kanji] = grade as keyof typeof JOYO_KANJI_LISTS;
  });
});

// Joyo grade counts for validation
export const JOYO_TOTALS = {
  '1': 80,
  '2': 160,
  '3': 200,
  '4': 200,
  '5': 185,
  '6': 181,
  '9': 1130, // Secondary school (grades 7-12)
} as const;

/**
 * Get Joyo grade for a kanji character
 * @param kanji The kanji character to look up
 * @returns Joyo grade ('1', '2', '3', '4', '5', '6', '9') or null if not found
 */
export function getJoyoGrade(kanji: string): keyof typeof JOYO_KANJI_LISTS | null {
  return KANJI_TO_JOYO[kanji] || null;
}

/**
 * Check if a kanji is in Joyo
 * @param kanji The kanji character to check
 * @returns true if the kanji is in any Joyo grade
 */
export function isJoyoKanji(kanji: string): boolean {
  return kanji in KANJI_TO_JOYO;
}

/**
 * Get all kanji for a specific Joyo grade
 * @param grade Joyo grade ('1', '2', '3', '4', '5', '6', '9')
 * @returns Array of kanji for that grade
 */
export function getKanjiForGrade(grade: keyof typeof JOYO_KANJI_LISTS): readonly string[] {
  return JOYO_KANJI_LISTS[grade];
}

/**
 * Get a human-readable name for a Joyo grade
 * @param grade Joyo grade ('1', '2', '3', '4', '5', '6', '9')
 * @returns Human-readable grade name
 */
export function getJoyoGradeName(grade: keyof typeof JOYO_KANJI_LISTS): string {
  if (grade === '9') return 'Grade 9';
  return `Grade ${grade}`;
}

/**
 * Get a compact label for a Joyo grade (for segmented controls)
 * @param grade Joyo grade ('1', '2', '3', '4', '5', '6', '9')
 * @returns Compact grade label
 */
export function getJoyoGradeLabel(grade: keyof typeof JOYO_KANJI_LISTS): string {
  if (grade === '9') return 'G9';
  return `G${grade}`;
}